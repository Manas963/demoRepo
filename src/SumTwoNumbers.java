public class SumTwoNumbers {
    public static int sum(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        int first = 10;
        int second = 20;
        int result = sum(first, second);

        System.out.println("Sum of " + first + " and " + second + " is: " + result);
    }
}
